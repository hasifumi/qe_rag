"""テスト用サンプル文書生成スクリプト（一時使用）"""
from pathlib import Path
from docx import Document
from docx.shared import Pt as DocxPt
from pptx import Presentation
from pptx.util import Pt

BASE = Path(__file__).parent / "docs"


# ── ヘルパー ────────────────────────────────────────────

def docx(path: Path, title: str, sections: list[tuple[str, str]]):
    doc = Document()
    doc.add_heading(title, 0)
    for heading, body in sections:
        doc.add_heading(heading, 1)
        for para in body.strip().split("\n"):
            doc.add_paragraph(para.strip())
    doc.save(path)
    print(f"  [docx] {path.relative_to(BASE.parent)}")


def pptx(path: Path, title: str, slides: list[tuple[str, str]]):
    prs = Presentation()
    sl = prs.slides.add_slide(prs.slide_layouts[0])
    sl.shapes.title.text = title
    sl.placeholders[1].text = ""
    for stitle, body in slides:
        sl = prs.slides.add_slide(prs.slide_layouts[1])
        sl.shapes.title.text = stitle
        sl.placeholders[1].text = body
    prs.save(path)
    print(f"  [pptx] {path.relative_to(BASE.parent)}")


def pdf(path: Path, title: str, sections: list[tuple[str, str]]):
    """PDF の代わりに同内容の docx を生成（拡張子は .pdf に変更しない）"""
    # fpdf2 の Windows 日本語フォント処理が不安定なため docx で代替
    docx_path = path.with_suffix(".docx")
    docx(docx_path, title, sections)
    print(f"  [note] {path.name} は .docx として保存")


def txt(path: Path, title: str, body: str):
    path.write_text(f"{title}\n{'=' * 40}\n\n{body.strip()}\n", encoding="utf-8")
    print(f"  [txt]  {path.relative_to(BASE.parent)}")


